struct Vector
{
    float x;
    float y;
    float z;
    Vector(float xIn, float yIn, float zIn)
    {
        this->x = xIn;
        this->y = yIn;
        this->z = zIn;
    }

    float getLength()
    {
        return sqrtf((x * x) + (y * y) + (z * z));
    };
    int normalize()
    {
        float length = getLength();
        float normalized = (1 / length) * (this->x + this->y + this->z);

        return normalized;
    }
};